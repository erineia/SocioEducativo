package br.com.jus.data.pojo.guia;

public class AtosInfracionais {

    private String codigoTpu;
    private String dataFato;
    private String descricao;
    private String localFato;


    public String getCodigoTpu() {
        return codigoTpu;
    }

    public void setCodigoTpu(String codigoTpu) {
        this.codigoTpu = codigoTpu;
    }

    public String getDataFato() {
        return dataFato;
    }

    public void setDataFato(String dataFato) {
        this.dataFato = dataFato;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getLocalFato() {
        return localFato;
    }

    public void setLocalFato(String localFato) {
        this.localFato = localFato;
    }
}
